package com.mycompany.nivelamento.java;

public class TiposJava {

    // método main()
    // (psvm e depois tecla <TAB>)
    public static void main(String[] args) {
        
        // sout e depois tecla <TAB>
        System.out.println("É nóis no Java");
        
        // tipos Java
        // Java é uma linguagem de Tipagem Estática
        
        // Detalhe: A convenção de nomes de variáveis
        // é o padrão camelCase
        
        // Tipo Texto: String
        String cidade = "São Paulo";
        
        // Tipo Númerico Inteiro: Integer
        Integer populacao = 15000000; // pode ser 15_000_000
        
        // Tipo Numérico Real: Double
        Double temperaturaMedia = 23.1234;
        
        // Tipo Boleano (ou Lógico): Boolean
        Boolean vivo = true;
        Boolean temMundial = false;
        
        
    }
    
}
